# Programa que transforma la cadena de texto "Pythoneros" a mayúsculas, guarda el valor en una variable
# y posteriormente la convierte nuevamente a minúsculas

texto = "Pythoneros"
texto_mayúsculas = texto.upper()
texto_minúsculas = texto_mayúsculas.lower()

print(f"En mayúsculas: {texto_mayúsculas}")
print(f"En minúsculas: {texto_minúsculas}")